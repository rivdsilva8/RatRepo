// Convert CSV string into an array of rat sighting objects
function parseCsvToRatSightings(csv) {
  // Split CSV into rows
  const rows = csv.trim().split("\n");

  // Extract the header row
  const headers = rows.shift().split(",");

  // Map each row into a rat sighting object
  const ratSightings = rows.map((row) => {
    const values = row.split(",");
    const ratSighting = {};

    // Map each value to its corresponding header
    headers.forEach((header, index) => {
      ratSighting[header.trim()] = isNaN(values[index])
        ? values[index].trim()
        : Number(values[index].trim());
    });

    return ratSighting;
  });

  return ratSightings;
}

